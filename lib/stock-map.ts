// 역방향 매핑 (코드 → 이름) 생성
const CODE_TO_NAME: Record<string, string> = {};
for (const [name, code] of Object.entries(STOCK_MAP)) {
  // 중복 코드의 경우 첫 번째(기본) 이름 사용
  if (!CODE_TO_NAME[code]) {
    CODE_TO_NAME[code] = name;
  }
}

/**
 * 종목명을 종목코드로 변환
 * @param input 종목명 또는 종목코드
 * @returns { code, name } 또는 null
 */
export function resolveStock(input: string): { code: string; name: string } | null {
  const trimmed = input.trim();

  // 이미 6자리 숫자(종목코드)인 경우
  if (/^\d{6}$/.test(trimmed)) {
    const name = CODE_TO_NAME[trimmed];
    return { code: trimmed, name: name || trimmed };
  }

  // 정확히 일치하는 종목명
  if (STOCK_MAP[trimmed]) {
    return { code: STOCK_MAP[trimmed], name: trimmed };
  }

  // 부분 매칭 (입력값을 포함하는 종목명)
  const partialMatch = Object.entries(STOCK_MAP).find(
    ([name]) => name.includes(trimmed) || trimmed.includes(name)
  );
  if (partialMatch) {
    return { code: partialMatch[1], name: partialMatch[0] };
  }

  return null;
}

/**
 * 코드로 종목명 조회
 */
export function getStockName(code: string): string {
  return CODE_TO_NAME[code] || code;
}
